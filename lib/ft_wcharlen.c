#include "../ft_printf.h"

size_t	ft_wcharlen(wchar_t c)
{
	size_t i;

	i = 0;
	if (c <= 0x7F)
		i++;
	else if (c <= 0x7FF)
		i += 2;
	else if (c <= 0xFFFF)
		i += 3;
	else if (c <= 0x10FFFF)
		i += 4;
	return (i);
}
